#include <stdio.h>

int main () {
	int a, b;
	
	printf("Bilangan pertama : ");
	scanf("%d", &a);
	
	printf("Bilangan kedua\t : ");
	scanf("%d", &b);
	
	printf("Hasil A + B = %i\n", a + b);
	printf("Hasil A - B = %i\n", a - b);
	printf("Hasil A * B = %i\n", a * b);
	printf("Hasil A / B = %i\n", a / b);
	printf("Hasil A % B = %i\n", a % b);
	
}
